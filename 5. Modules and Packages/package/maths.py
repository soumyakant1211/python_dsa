def addition(a, b):
    """
    This function takes two numbers and returns the sum of them.
    Args:
        a (int/float): The first number.
        b (int/float): The second number.
    Returns:
        int/float: The sum of a and b.
    """
    return a + b

def subtraction(a, b):
    """
    This function takes two numbers and returns the difference between them.
    Args:
        a (int/float): The first number.
        b (int/float): The second number.
    Returns:
        int/float: The difference of a and b.
    """
    return a - b